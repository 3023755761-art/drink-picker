const CATEGORY_SLUG = {
  'The Unforgettables':   'the-unforgettables',
  'Contemporary Classics': 'contemporary-classics',
  'New Era Drinks':        'new-era',
}

// IBA 网站上部分鸡尾酒的图片文件名 slug 与 cocktail id 不一致
const SLUG_FIX = {
  've-n-to':             'vento',
  'grand-margarita':     'tommys-margarita',
  'irish-coffee':        'irish-coffee-copy',
  'remember-the-maine':  'remember-the-main',
}

function imageUrl(cocktail) {
  const key = cocktail.id
  const cat = CATEGORY_SLUG[cocktail.category] || 'the-unforgettables'
  const slug = SLUG_FIX[key] || key
  if (CONFIRMED_HASHES[key]) {
    return `${IBA_IMAGE_BASE}/iba-cocktail-${cat}-${slug}-${CONFIRMED_HASHES[key]}.webp`
  }
  // 未确认hash的鸡尾酒 — 运行 scripts/fetch-images.mjs 可批量获取
  return `${IBA_IMAGE_BASE}/iba-cocktail-${cat}-${slug}-PLACEHOLDER.webp`
}

